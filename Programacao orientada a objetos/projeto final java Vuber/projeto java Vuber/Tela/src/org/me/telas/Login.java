    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        
        resultado = usuarioCon.getCpfUsuarioLogin(cpf.getText());
        numero = resultado.size();
        if (numero!=0){
            indice = 0;
            indiceEntrada = resultado.get(indice);
            txtCpf.setText(indiceEntrada.getCpf());
            txtSenha.setText(indiceEntrada.getSenha());
            txtTipo_conta.setText(indiceEntrada.getTipo_conta());
        }
        
        if (cpf.getText().equals(txtCpf.getText()) && senha.getText().equals(txtSenha.getText())){
            if (txtTipo_conta.getText().equals("aluno")){
                TelaAluno ta = new TelaAluno();
                ta.setVisible(true);
                this.dispose();
            }else if (txtTipo_conta.getText().equals("dono")){
                TelaDono td = new TelaDono();
                td.setVisible(true);
                this.dispose();
            }else if (txtTipo_conta.getText().equals("motorista")){
                TelaMotorista tm = new TelaMotorista();
                tm.setVisible(true);
                this.dispose();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Usuario n√£o encontrado", "Erro", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_entrarActionPerformed

